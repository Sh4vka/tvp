/*
* Программа 02-2.c
* Иллюстрирует работу с pipe в рамках одного процесса.
*
* Демонстрирует:
* - создание pipe (системный вызов pipe())
* - запись данных в канал (write)
* - чтение данных из канала (read)
* - закрытие дескрипторов
*/
#include <sys/types.h> // Для size_t и других системных типов
#include <unistd.h> // Для pipe(), read(), write(), close()
#include <stdio.h> // Для printf()
#include <stdlib.h> // Для exit()
int main() {
    int fd[2]; // Массив из двух файловых дескрипторов:
    // fd[0] - для чтения из канала
    // fd[1] - для записи в канал
    size_t size; // Количество реально прочитанных/записанных байт
    char string[] = "Hello, world!"; // Исходная строка (14 байт)
    char resstring[14]; // Буфер для приёма данных
    /* 1 СОЗДАНИЕ PIPE */
    if (pipe(fd) < 0) {
    /* Если создать pipe не удалось (например, превышен лимит открытых
    файлов или не хватает памяти в ядре), выводим сообщение и выходим */
        printf("Can't create pipe\n");
        exit(-1);
    }
    /* 2 ЗАПИСЬ ДАННЫХ В PIPE */
    /* Пытаемся записать в канал 14 байт из массива string.
    Запись производится в дескриптор fd[1] (входной конец трубы). */
    size = write(fd[1], string, 14);
    if (size != 14) {
    /* Для pipe такая ситуация маловероятна (если только канал не закрыт
    на чтение или не случилась ошибка), но проверка обязательна */
        printf("Can't write all string to pipe\n");
        exit(-1);
    }
    /* 3 ЧТЕНИЕ ДАННЫХ ИЗ PIPE */
    /* Пытаемся прочитать из канала 14 байт в массив resstring.
    Чтение производится из дескриптора fd[0] (выходной конец трубы). */
    size = read(fd[0], resstring, 14);
    if (size < 0) {
        printf("Can't read string from pipe\n");
        exit(-1);
    }
    /* 4 ВЫВОД РЕЗУЛЬТАТА */
    printf("%s\n", resstring);
    /* 5 ЗАКРЫТИЕ ДЕСКРИПТОРОВ */
    close(fd[0]);
    close(fd[1]);
    return 0;
}
